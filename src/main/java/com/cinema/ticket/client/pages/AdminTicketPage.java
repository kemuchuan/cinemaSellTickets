    public static AdminTicketPage getAdminTicket(Ticket ticket, int x, int y){
        // 设置不可见
        adminTicket.setVisible(false);
        // 设置出现位置
        adminTicket.setLocation(x,y);
        // 设置待更改的票数据对象
        adminTicket.ticket = ticket;
        // 设置票信息的展示
        adminTicket.ticketLabel.setText("该座位坐标位: 第 "+(ticket.getLocX()+1)+" 排, 第 "+(ticket.getLocY()+1)+"列");
        adminTicket.ticketLabel.setVisible(true);
        // 若票没有票ID，则设置相应的属性
        if(ticket.getTicketId()==null){
            // 未出售单选按钮选中
            adminTicket.noSaleRadio.setSelected(true);
            // 设置价格框为空
            adminTicket.priceText.setText("");
        }else{
            // 票有ID
            // 出售单选按钮选中
            adminTicket.saleRadio.setSelected(true);
            // 设置价格
            adminTicket.priceText.setText(ticket.getPrice()+"");
        }
        return adminTicket;
    }

