    // 确认按钮点击方法
    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        // 判断是出售还是不出售
        if(this.saleRadio.isSelected()){
            // 设置票信息
            this.ticket.setPrice(Double.parseDouble(this.priceText.getText()));
            if(ticket.getTicketId()!=null){
                // 判断票是否有ID
                // 有更新
                this.ticketApi.updateTicket(ticket);
            }else{
                // 无删除
                this.ticketApi.insertTicket(ticket);
            }
        }else{
            // 不出售，若票有ID则删除
            if(ticket.getTicketId()!=null){
                this.ticketApi.deleteTicket(ticket.getTicketId());
            }
        }
        // 设置窗口不可见
        this.setVisible(false);
    }//GEN-LAST:event_confirmBtnActionPerformed

